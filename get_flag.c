#include "ft_printf.h"

int		get_flag(char **f)
{
	int	out;

	out = 0;
	if (**f == '0')
	{
		out += 1;
		(*f)++;
	}
	if (**f == '-')
	{
		out += 2;
		(*f)++;
	}
	if (**f == ' ')
	{
		out += 4;
		(*f)++;
	}
	if (**f == '+')
	{
		out += 8;
		(*f)++;
	}
	if (**f == '#')
	{
		out += 16;
		(*f)++;
	}
	return (out);
}
