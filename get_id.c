#include "ft_printf.h"

int		get_id(const char *f, char *id)
{
	while (*f && validate(*f, "#-+ 1234567890."))
		f++;
	if (!validate(*f, "sSoOxXuUcCdDiIfF"))
		return (-1);
	*id = *f;
	return (0);
}
